package we_chat.pojo;

public class NewInform {

    private String id;
    private String fId;
    private String fName;
    private String content;
    private String jId;
    private String state;
    private String findstate;
    private String informImage;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getfId() {
        return fId;
    }

    public void setfId(String fId) {
        this.fId = fId;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getjId() {
        return jId;
    }

    public void setjId(String jId) {
        this.jId = jId;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getFindstate() {
        return findstate;
    }

    public void setFindstate(String findstate) {
        this.findstate = findstate;
    }

    public String getInformImage() {
        return informImage;
    }

    public void setInformImage(String informImage) {
        this.informImage = informImage;
    }
}
