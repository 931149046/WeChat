package we_chat.pojo;


public class CircleNew {
  private String id;
  private String name; //发说说的用户名
  private String image; //发说的头像
  private String content; //发的内容
  private String time; //发说说的时间
  private String address; //发说说的地址
  private String cimage; //发说说的图片

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getImage() {
    return image;
  }

  public void setImage(String image) {
    this.image = image;
  }

  public String getContent() {
    return content;
  }

  public void setContent(String content) {
    this.content = content;
  }

  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }

  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }

  public String getCimage() {
    return cimage;
  }

  public void setCimage(String cimage) {
    this.cimage = cimage;
  }
}
